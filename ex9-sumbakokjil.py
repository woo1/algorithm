# 숨바꼭질 BFS
from collections import deque

def bfs():
    q = deque()
    q.append(N)
    while q:
        # print('find', q)
        v = q.popleft()
        if v == K:
            # print(time[v])
            return
        for next_step in (v-1, v+1, v*2):
            if 0 <= next_step < MAX and not time[next_step]: # 이전에 방문한 적 없는 장소
                time[next_step] = time[v] + 1
                q.append(next_step)

        # print(q)

# 0 ~ 100,000
MAX = 100001
N, K = map(int, input().split())
time = [0]*MAX
bfs()


